void main() {
    IO.println("Ch03 - Arithmetic\n\n");

    int x = 11;
    int y = 2;
    int z;

    z = x + y;

    IO.println(z);

    z = x * y;

    IO.println(z);

    z = x / y;

    IO.println(z);

    z = x % y;

    IO.println(z);

    x = x / y;
    IO.println(x);
    x += y;
    IO.println(x);
}
